class Durgamath:
    @staticmethod
    def add(x,y):
        print('The sum:',x+y)
    @staticmethod
    def product(x,y):
        print('The Product:',x*y)
    @staticmethod
    def average(x,y):
        print('The Product:',(x+y)/2)
Durgamath.add(10,20)
Durgamath.product(10,20)
Durgamath.average(10,20)